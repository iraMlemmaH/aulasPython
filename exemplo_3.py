#Solicitar os dados para o usuário
v1 = int(input("\n\nInforme o primeiro número inteiro: "))
v2 = int(input("Informe o segundo número inteiro: "))
print("\n")
#Exibindo o resultado das operações matemáticas
print("•–––––-☆Sistema Para Calcular as Operações Básicas☆-----––•\n")

#SOMA
resultado = v1 + v2
print(f"Resultado da soma: {resultado}")

#SUBTRAÇÃO
resultado = v1 - v2
print(f"Resultado da subtração: {resultado}")

#MULTIPLICAÇÃO
resultado = v1 * v2
print(f"Resultado da multiplicação: {resultado}")

#DIVISÃO
resultado = v1 / v2
print(f"Resultado da multiplicação: {resultado:.2f}\n")
